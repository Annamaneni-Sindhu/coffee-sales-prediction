import pandas as pd
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression

df=pd.read_excel("coffee predict.xlsx")

x=df[["temperature"]]
y=df["cups_sold"]

model=LinearRegression()
model.fit(x,y)

temp_pre=[[24]]
predict=model.predict(temp_pre)
print(f"Predicted cups are{temp_pre[0][0]}C:{round(predict[0])}")

plt.scatter(df["temperature"],df["cups_sold"],color="blue",label="Actual data")
plt.plot(df["temperature"],model.predict(x),color="green",label="prediction line")
plt.scatter(temp_pre,predict,color="red",s=100,label="New Prediction")
plt.xlabel("Temperature")
plt.ylabel("Cups sold")
plt.title("Coffee saled prediction")
plt.legend()
plt.show()